def split_text_for_telegram(text, limit=3500):
    if len(text) <= limit:
        return [text]

    parts = []
    current = ""

    chunks = text.split("\n\n")
    for chunk in chunks:
        candidate = chunk if not current else current + "\n\n" + chunk
        if len(candidate) <= limit:
            current = candidate
        else:
            if current:
                parts.append(current)
            if len(chunk) <= limit:
                current = chunk
            else:
                lines = chunk.split("\n")
                temp = ""
                for line in lines:
                    cand = line if not temp else temp + "\n" + line
                    if len(cand) <= limit:
                        temp = cand
                    else:
                        if temp:
                            parts.append(temp)
                        temp = line
                current = temp

    if current:
        parts.append(current)

    return parts


def send_message(chat_id, text, reply_markup=None):
    parts = split_text_for_telegram(text)

    for i, part in enumerate(parts):
        data = {
            "chat_id": chat_id,
            "text": part
        }
        if i == len(parts) - 1 and reply_markup:
            data["reply_markup"] = reply_markup

        requests.post(
            f"{URL}/sendMessage",
            json=data,
            timeout=15
        )


