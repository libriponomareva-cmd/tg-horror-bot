def empty_data() -> dict:
    return {"users": {}, "subscribers": []}


def load_data() -> dict:
    if not os.path.exists(DATA_FILE):
        return empty_data()

    try:
        with open(DATA_FILE, "r", encoding="utf-8") as f:
            data = json.load(f)
    except Exception:
        return empty_data()

    if "users" not in data:
        data["users"] = {}
    if "subscribers" not in data:
        data["subscribers"] = []

    return data


DATA = load_data()


def save_data() -> None:
    with DATA_LOCK:
        with open(DATA_FILE, "w", encoding="utf-8") as f:
            json.dump(DATA, f, ensure_ascii=False, indent=2)


def get_user(chat_id) -> dict:
    chat_id = str(chat_id)

    if chat_id not in DATA["users"]:
        DATA["users"][chat_id] = {
            "scene": "S1",
            "stats": default_stats()
        }
        save_data()

    user = DATA["users"][chat_id]

    if "scene" not in user:
        user["scene"] = "S1"

    if "stats" not in user:
        user["stats"] = default_stats()

    for stat_name, default_value in default_stats().items():
        if stat_name not in user["stats"]:
            user["stats"][stat_name] = default_value

    return user


def reset_user(chat_id) -> dict:
    chat_id = str(chat_id)
    DATA["users"][chat_id] = {
        "scene": "S1",
        "stats": default_stats()
    }
    save_data()
    return DATA["users"][chat_id]


def apply_stats(chat_id, changes: dict) -> None:
    user = get_user(chat_id)

    for stat_name, value in changes.items():
        stat_name = normalize_stat_name(stat_name)

        if stat_name not in user["stats"]:
            user["stats"][stat_name] = 0

        user["stats"][stat_name] += value

    save_data()


def set_stats(chat_id, changes: dict) -> None:
    user = get_user(chat_id)

    for stat_name, value in changes.items():
        stat_name = normalize_stat_name(stat_name)
        user["stats"][stat_name] = value

    save_data()


def set_scene(chat_id, scene: str) -> None:
    user = get_user(chat_id)
    user["scene"] = scene
    save_data()


def subscribe_user(chat_id) -> bool:
    chat_id = str(chat_id)

    if chat_id not in DATA["subscribers"]:
        DATA["subscribers"].append(chat_id)
        save_data()
        return True

    return False


