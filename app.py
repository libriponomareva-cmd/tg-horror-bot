def apply_stats(chat_id, changes):
    init_user(chat_id)
    for stat_name, value in changes.items():
        if stat_name not in user_stats[chat_id]:
            user_stats[chat_id][stat_name] = 0
        user_stats[chat_id][stat_name] += value


